SELECT
    a.id,
    b.id,
    'flight',
    ROUND((2.0 + (
        6371 * 2 * ASIN(SQRT(LEAST(1,
            POWER(SIN(RADIANS(b.latitude - a.latitude) / 2), 2) +
            COS(RADIANS(a.latitude)) * COS(RADIANS(b.latitude)) *
            POWER(SIN(RADIANS(b.longitude - a.longitude) / 2), 2)
        ))) / 650))::numeric, 2),
    CASE
        WHEN (6371 * 2 * ASIN(SQRT(LEAST(1,
            POWER(SIN(RADIANS(b.latitude - a.latitude) / 2), 2) +
            COS(RADIANS(a.latitude)) * COS(RADIANS(b.latitude)) *
            POWER(SIN(RADIANS(b.longitude - a.longitude) / 2), 2)
        )))) < 300 THEN 1200000
        WHEN (6371 * 2 * ASIN(SQRT(LEAST(1,
            POWER(SIN(RADIANS(b.latitude - a.latitude) / 2), 2) +
            COS(RADIANS(a.latitude)) * COS(RADIANS(b.latitude)) *
            POWER(SIN(RADIANS(b.longitude - a.longitude) / 2), 2)
        )))) < 700 THEN 1800000
        WHEN (6371 * 2 * ASIN(SQRT(LEAST(1,
            POWER(SIN(RADIANS(b.latitude - a.latitude) / 2), 2) +
            COS(RADIANS(a.latitude)) * COS(RADIANS(b.latitude)) *
            POWER(SIN(RADIANS(b.longitude - a.longitude) / 2), 2)
